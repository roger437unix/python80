import math
n1 = float(input('\nInforme um número: '))
n2 = float(input('Informe outro número: '))

soma = n1 + n2
print(f'\n{n1} + {n2} = {soma}')
print(f'{n1} - {n2} = {n1 - n2}')
print(f'{n1} x {n2} = {n1 * n2}')
print(f'{n1} : {n2} = {n1 / n2}')
print(f'{n1} ^ {n2} = {n1 ** n2}')
print(f'{n1} mod {n2} = {n1 % n2}')
print(f'Raiz quadrada de {n1} = {math.sqrt(n1)}')
print(f'Raiz quadrada de {n2} = {math.sqrt(n2)}')